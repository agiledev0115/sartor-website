        function getSiblings(element, type) {
          var arraySib = [];
          if (type == "prev") {
            while ((element = element.previousElementSibling)) {
              arraySib.push(element);
            }
          } else if (type == "next") {
            while ((element = element.nextElementSibling)) {
              arraySib.push(element);
            }
          }
          return arraySib;
        }

        const cardDeck = document.getElementById("cardDeck");
        const cards = document.querySelectorAll(".card");
        var focused = false;
        var hovered = false;

        cards.forEach(card => {
          card.addEventListener("mouseenter", function () {
            let allNext = getSiblings(this, "next");

            allNext.forEach(el => {
              el.style.setProperty(
                "--translate",
                "calc(170px * var(--relation))"
              );
            });
          });
          card.addEventListener("mouseleave", function () {
            let allNext = getSiblings(this, "next");

            allNext.forEach(el => {
              el.style.setProperty("--translate", "0px");
            });
          });
        });

        cardDeck.addEventListener("mouseover", function() {
            hovered = true;
            focused = true;
        });
        cardDeck.addEventListener("mouseout", function() {
            hovered = false;
            focused = false;
        });
        cardDeck.addEventListener("mousedown", function() {
            focused = true;
        });
        cardDeck.addEventListener("mouseup", function() {
            if (!hovered) {
                focused = false;
            }
        });
        cardDeck.addEventListener("touchstart", function() {
            focused = true;
        });
        cardDeck.addEventListener("touchend", function() {
            focused = false;
        });

        setInterval(() => {
            if (!focused) {
                document.getElementById('cardDeck').scrollLeft += 100;
                if (cardDeck.scrollLeft + window.innerWidth >= cardDeck.scrollWidth) {
                    document.getElementById('cardDeck').scrollLeft = 0;
                }
            }
        }, 1000);
